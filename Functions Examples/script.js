//	Loops

//	While Loop
console.log("---------- Loops ----------");
var b = 10;

while (b > 0){
	console.log(b + " kegs on the wall.");
	b--;
}


//  Do-while Loop
console.log("---------- Do While Loops ----------");
var c = 10;

do {
	console.log(c + " kegs on the wall.");
	c--;
}
while(c >0);

//  For Loops
console.log("---------- For Loops ----------");

for(var i= 10; i > 0; i--){
	console.log(i + "kegs on the wall.")
}